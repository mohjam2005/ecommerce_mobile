    public function loginSubmit(Request $request){
        $request->validate([
            'email' => 'required|email',
            'password' => 'required',
        ]);

        $user = User::where('email', $request->email)->first();

        if (!$user) {
            return redirect()->back()->withErrors(['email' => 'این ایمیل ثبت العميل نشده است.']);
        }

        if (!Hash::check($request->password, $user->password)) {
            return redirect()->back()->withErrors(['password' => 'رمز عبور شما اشتباه است.']);
        }

        if (Auth::attempt(['email' => $request->email, 'password' => $request->password, 'status' => 'active'])) {
            Session::put('user', $request->email);
            request()->session()->flash('success', 'با موفقیت وارد شدید');
            return redirect()->route('home');
        } else {
            return redirect()->back()->withErrors(['email' => 'حساب مستخدمی شما فعال نیست.']);
        }
    }

